// Подключаем библиотеку для ввода/вывода данных
#include <iostream>
// Подключаем библиотеку для работы с динамическими массивами (векторами)
#include <vector>
// Используем стандартное пространство имен, чтобы не писать std:: перед каждой командой
using namespace std;

// Главная функция программы - отсюда начинается выполнение
int main54534() {
    // Объявляем три переменные для хранения входных данных
    int a, b, k;
    // Считываем три числа из входного потока (из файла или с клавиатуры)
    // Первое число попадет в a, второе в b, третье в k
    cin >> a >> b >> k;

    // Создаем вектор (динамический массив) типа bool размером b+1
    // Изначально все элементы устанавливаем в true
    // isPrime[i] будет хранить true, если число i простое, и false - если составное
    vector<bool> isPrime(b + 1, true);

    // Число 0 не является простым - помечаем его как составное
    isPrime[0] = false;
    // Число 1 не является простым - помечаем его как составное
    isPrime[1] = false;

    // Перебираем все числа от 2 до квадратного корня из b
    // Почему до sqrt(b)? Потому что если число составное, то у него есть делитель <= sqrt(числа)
    for (int i = 2; i * i <= b; i++) {
        // Проверяем, является ли текущее число i простым
        // Если isPrime[i] = true, значит мы еще не вычеркнули это число
        if (isPrime[i]) {
            // Если i простое, то ВСЕ числа, кратные i (кроме самого i) - составные
            // Начинаем вычеркивать с i*i, потому что все меньшие кратные числа уже были вычеркнуты ранее
            // Например, для i=5: 5*2=10 уже вычеркнуто при i=2, 5*3=15 при i=3, 5*4=20 при i=2
            for (int j = i * i; j <= b; j += i) {
                // Помечаем число j как составное
                isPrime[j] = false;
            }
        }
    }

    // Счетчик простых чисел, которые мы уже нашли в диапазоне [a, b]
    int count = 0;

    // Перебираем все числа от a до b включительно
    for (int i = a; i <= b; i++) {
        // Проверяем, является ли текущее число i простым
        if (isPrime[i]) {
            // Увеличиваем счетчик найденных простых чисел
            count++;

            // Проверяем, достигли ли мы k-го простого числа
            if (count == k) {
                // Если да, выводим это число и завершаем поиск
                cout << i << endl;
                break;  // Прерываем цикл, так как нашли то, что нужно
            }
        }
    }

    // Возвращаем 0, что означает успешное завершение программы
    return 0;
}